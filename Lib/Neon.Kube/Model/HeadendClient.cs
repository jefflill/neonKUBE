using System;
using System.Net.Http;

using Neon.Common;
using Neon.Retry;

using Newtonsoft.Json;
using Newtonsoft.Json.Converters;
using Newtonsoft.Json.Serialization;

using YamlDotNet.Serialization;

namespace Neon.Kube.Clients
{
    /// <summary>
    /// Extends the <see cref="HeadendClient"/> class generated by <b>modelgen</b>
    /// with static creation methods.
    /// </summary>
    public partial class HeadendClient
    {
        /// <summary>
        /// Creates a <see cref="HeadendClient"/>.
        /// </summary>
        /// <param name="baseAddress">
        /// Optionally specifies the base URI for the client.  This defaults to the
        /// headend service URI returned by <see cref="KubeEnv.HeadendUri"/>.
        /// </param>
        /// <param name="retryPolicy">Optionally specifies a default retry policy (defaults to <see cref="NoRetryPolicy"/>).</param>
        /// <returns>The new <see cref="HeadendClient"/>.</returns>
        public static HeadendClient Create(Uri baseAddress = null, IRetryPolicy retryPolicy = null)
        {
            baseAddress ??= KubeEnv.HeadendUri;

            return new HeadendClient(new HttpClient() { BaseAddress = baseAddress }, retryPolicy);
        }
    }
}
